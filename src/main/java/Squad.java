public class Squad {
    private String squadName;
    private String squadCause;
    private int squadsize;

  
    public Squad(String name,String cause, int size) {
      squadName = name;
      squadCause = cause;
      squadsize = size;

    }
  
    public String getName() {
      return squadName;
    }
    public String getCause() {
        return squadName;
      }
      public String getSize() {
        return squadName;
      }
  }